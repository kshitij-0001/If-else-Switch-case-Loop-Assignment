package if_else_switch_loop;

public class NumberSignChecker {
	
	public static void main(String[]args) {
		int num=-1;
		
		if(num>=0) {
			if(num>0) {
				System.out.println("Positive number");
			}
			else {
				System.out.println("Zero");
			}
		}
		else {
			System.out.println("Negative");
		}
	}

}
